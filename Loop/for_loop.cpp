#include <iostream>
using namespace std;

int main() {

  	// for loop to print "Hi" 5 times
    for (int i = 0; i < 5; i++) { //set i=0;condition i<5; i = i+1 (loop for 5 time start with 0)
      	cout << "Hi => i = " << i << endl; // show text + i if condition is TRUE
    }
  
    return 0;
}